import pytest
from app.services.service_base import service_response
from http import HTTPStatus


def test_service_response_success(app):
    """
    Tests the service response function for a successful operation and
    ensures it returns the proper status code and response structure.

    Args:
        app: A test application or context required to run the service
            response.

    Raises:
        AssertionError: If the response structure, message, category,
            or status code do not match the expected values.
    """
    data = {"key": "value"}
    response, status_code = service_response(
        status_code=HTTPStatus.OK,
        message="Operation successful",
        category="success",
        data=data
    )

    assert status_code == HTTPStatus.OK
    assert response["data"] == data
    assert response["message"] == "Operation successful"
    assert response["category"] == "success"


def test_service_response_error(app):
    """
    Tests the response generated by the service_response function when the response
    indicates an error. This test ensures that the function returns the expected
    HTTP status code along with the properly formatted response dictionary
    containing a message, category, and data.

     Args:
        app: A test application or context required to run the service
            response.
    """
    response, status_code = service_response(
        status_code=HTTPStatus.NOT_FOUND,
        message="Not found",
        category="error",
        data=None
    )

    assert status_code == HTTPStatus.NOT_FOUND
    assert response["data"] is None
    assert response["message"] == "Not found"
    assert response["category"] == "error"


