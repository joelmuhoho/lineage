def test_service_response_invalid_status_code(app):
    """
    Tests the service_response function with an invalid status code.

    This function verifies that the service_response function raises a
    ValueError when provided with an invalid HTTP status code. It ensures
    proper validation of the status_code input.

    Args:
        app: Fixture representing the application context, typically
            used for testing purposes.

    Raises:
        ValueError: If the provided status_code is invalid, indicating
            an unsupported HTTP status code.

    """
    with pytest.raises(ValueError, match="Invalid HTTP status code"):
        service_response(
            status_code=99,
            message="Invalid input",
            category="error",
            data=None
        )


def test_service_response_invalid_category(app):
    """
    Tests the service_response function for handling invalid category input.

    This test ensures that the function raises a ValueError with the appropriate
    message when an invalid category is provided.

    Args:
        app: The test application or fixture required to invoke the service_response
        function in the context of the test environment.

    Raises:
        ValueError: If the category provided to service_response is not among the
        allowed values ("success", "error", "warning", "danger").
    """
    with pytest.raises(ValueError, match="Category must be one of: danger, error, success, warning"):
        service_response(
            status_code=HTTPStatus.OK,
            message="Invalid category",
            category="invalid",
            data=None
        )
